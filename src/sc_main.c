VOID ShowCheckResult(DWORD dwThreadId, ULONGLONG Start, ULONGLONG End, BOOL bGetPcFlag)
{
    HANDLE hStdout = GetStdHandle(STD_OUTPUT_HANDLE);

    printf("-------------------------Thread (%d)\n", dwThreadId);
    printf("Status      \t\t: Processed.\n");
    printf("GetPC Status\t\t: ");
    if ( bGetPcFlag )
    {
        SetConsoleTextAttribute(hStdout, FOREGROUND_RED | FOREGROUND_INTENSITY);
        printf("Detected\n");
    }
    else
    {
        SetConsoleTextAttribute(hStdout, FOREGROUND_GREEN | FOREGROUND_INTENSITY);
        printf("Not Detected\n");
    }

    SetConsoleTextAttribute(hStdout, FOREGROUND_RED | FOREGROUND_GREEN | FOREGROUND_BLUE);
    printf("Elapsed Time\t\t: %d Milliseconds.\n\n", (End - Start));
}

