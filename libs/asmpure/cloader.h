//---------------------------------------------------------------------
// CLink
//---------------------------------------------------------------------
struct CLink
{
	struct IQUEUEHEAD head;
	CEncoding encoding;
	unsigned long offset;
	int size;
	int lineno;
};

typedef struct CLink CLink;


//---------------------------------------------------------------------
// CLoader Structure
//---------------------------------------------------------------------
struct CLoader
{
	struct IQUEUEHEAD head;		// link head;
	char *error;
	int errcode;
	int lineno;
	unsigned long linear;
	unsigned char *output;
};

typedef struct CLoader CLoader;

